# Python Data Type Conversion
# We can Convert Standard Data Type with Standard Type Function such as: int(), float(), str()

# Conversion from Integer to Float
print(float(5))

# Conversion from Float to Integer ( works like floor/truncating )
print(int(10.6))
print(int(-10.6))

# Conversion from String to Float/Integer
print(float('2.5'))
print(int('25'))
print((float('2.5'))+(int('25')))

# Conversion from Integer/Float
print(str(2.5))
print(str(25))
print((str(2.5))+(str(25)))



