# Python Collection Conversion

# Conversion from List to Set
print(set([1, 2, 3]))

# Conversion from Set to Tuple
print(tuple({5, 6, 7}))

# Conversion from Tuple to List
print(list('hello'))

# Conversion from List to Dictionary
print(dict([[1, 2], [3, 4]]))

# Conversion from Tuple to Dictionary
print(dict([(3, 26), (4, 44)]))
